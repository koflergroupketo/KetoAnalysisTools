#' Webgestalt results give a link to view pathways where the metabolites and gene are highlighted
#' That same link can be used to get metabolite names from ChEBI database
#' For MetaboAnalyst results
#' @param link webgestalt link
#' @param ref_df dataframe
#'
#' @export
extract_link_entities <- function(link, ref_df) {
  # 1. Extract gene names (all-caps words like IL1A, HIF1A, VEGFA)
  gene_matches <- stringr::str_extract_all(link, "(?<==)[A-Z0-9]+(?=,|&|$)")[[1]]

  # 2. Extract ChEBI IDs (ChEBI_12345 or CHEBI:12345)
  chebi_matches <- stringr::str_extract_all(link, "(?<=ChEBI_)[0-9]+|(?<=CHEBI:)[0-9]+")[[1]]
  chebi_matches <- unique(na.omit(chebi_matches))
  print(chebi_matches)

  # 3. Lookup metabolite names
  ref_df$CHEBI <- as.character(ref_df$CHEBI)
  found <- dplyr::filter(ref_df, CHEBI %in% chebi_matches)

  # 4. Create a mapping: ChEBI ID → metabolite name
  met_map <- setNames(found$`Short.Name`, found$CHEBI)

  # 5. Combine gene names + metabolites (preserving order in link)
  # Build a single ordered vector of both types
  all_matches <- stringr::str_extract_all(
    link,
    "(?<=,|=)[A-Z0-9]+(?=,|&|$)|ChEBI_[0-9]+|CHEBI:[0-9]+"
  )[[1]]

  # Replace ChEBI IDs with names if available
  all_named <- sapply(all_matches, function(x) {
    # Normalize ChEBI ID formats
    id <- sub("ChEBI_|CHEBI:", "", x)
    if (id %in% names(met_map)) {
      return(met_map[id])
    } else {
      return(x)  # likely a gene name
    }
  })

  return(all_named)
}


