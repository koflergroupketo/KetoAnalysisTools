#' Given a list of metabolites short names
#' pull hmdb ids from annotation dataframe
#' Annotation dataframe created by Rudy and Daniela
#'
#' @param metab_list metabolite short name list
#' @param type whether metabolite list consist of "lipid" or "non-lipid" or
#' "none" in case we want all of them
#' @export
get_hmdb_ids <- function(metab_list, type){
  if (type != "none") {
    hmdb_column <-
      rudy_dani_hmdb |>
      dplyr::filter(source == type) |>
      dplyr::select(HMDB, Name, Short.Name, Associated_genes) |>
      dplyr::filter(Short.Name %in% metab_list) |>
      drop_na() |>
      dplyr::pull(HMDB) |>
      unique()
    return(hmdb_column)
  } else {
    hmdb_column <-
      rudy_dani_hmdb |>
      dplyr::select(HMDB, Name, Short.Name, Associated_genes) |>
      dplyr::filter(Short.Name %in% metab_list) |>
      drop_na() |>
      dplyr::pull(HMDB) |>
      unique()
    return(hmdb_column)
  }
}

