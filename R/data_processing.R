#' get the summary of correlation results
#'
#' @param corr_df Correlation results
#' @param top_n summary of top n results sorted by correlation coefficient
#' @param p_cutoff p-value cutoff currently set to 0.05
#' @param cor_cutoff cor_cutoff cutoff currently set to 0.7 positive or negative
#' @param slope_cutoff slope_cutoff cutoff currently set to 1 positive or negative
#'
#' @export
summarize_corr <- function(corr_df, top_n = 100,
                           p_cutoff = 0.05, cor_cutoff = 0.7, slope_cutoff = 1) {
  setDT(corr_df)

  # Filter significant pairs
  filtered <- corr_df %>%
    filter(p_value < p_cutoff,
           abs(cor) > cor_cutoff,
           abs(slope) > slope_cutoff)

  # Top N by absolute correlation
  topN <- filtered %>%
    arrange(desc(abs(cor))) %>%
    head(top_n)

  # 1. Count positive vs negative correlations
  pos_neg_summary <- topN %>%
    summarise(
      positive = sum(cor > 0),
      negative = sum(cor < 0)
    )

  # 2. Most connected metabolites
  metabolite_summary <- topN %>%
    group_by(Metabolite) %>%
    summarise(
      n_connections = n(),
      n_positive = sum(cor > 0),
      n_negative = sum(cor < 0)
    ) %>%
    arrange(desc(n_connections))

  # 3. Most connected genes
  gene_summary <- topN %>%
    group_by(GeneSymbol) %>%
    summarise(
      n_connections = n(),
      n_positive = sum(cor > 0),
      n_negative = sum(cor < 0)
    ) %>%
    arrange(desc(n_connections))

  # Return everything as a list
  return(list(
    n_total_filtered = nrow(filtered),
    pos_neg_summary = pos_neg_summary,
    top_metabolites = head(metabolite_summary, 10),
    top_genes = head(gene_summary, 10),
    top_pairs = topN
  ))
}

#' Summarize correlation
#'
#' @export
# Function to summarize correlations per xenograft
summarize_corr2 <- function(df, xenograft_name, thresholds = seq(0.7, 0.9, by = 0.01)) {
  # Ensure numeric
  df <- df %>%
    mutate(cor = as.numeric(cor),
           slope = as.numeric(slope),
           p_value = as.numeric(p_value))

  # Apply significance filter
  df <- df %>%
    filter(abs(slope) > 1, p_value < 0.05)

  # Calculate number of edges at different thresholds
  counts <- lapply(thresholds, function(th) {
    n <- sum(abs(df$cor) > th, na.rm = TRUE)
    data.frame(Xenograft = xenograft_name, Threshold = th, N_edges = n)
  }) %>%
    bind_rows()

  list(filtered = df, counts = counts)
}

combine_pvalues <- function(p) {
  chi_sq <- -2 * sum(log(p), na.rm = TRUE)
  df <- 2 * sum(!is.na(p))
  pchisq(chi_sq, df = df, lower.tail = FALSE)
}


#' get KEGG class and subclass information in order to group pathways into subclass
#' For webgestalt results
#' @param webgestalt webgestalt results
#' @param organism_prefix currently set to human "hsa"
#'
#' @export
webgestalt_kegg_class <- function(webgestalt, organism_prefix = "hsa") {
  pathway_ids <- unique(webgestalt$geneSet)
  # Function to fetch classification info for a vector of KEGG pathway IDs
  out_list <- lapply(pathway_ids, function(pid) {
    message("Fetching ", pid, " ...")
    res <- tryCatch(
      keggGet(pid)[[1]],
      error = function(e) NULL
    )
    if (is.null(res)) {
      return(tibble(
        geneSet = pid,
        kegg_name = NA_character_,
        CLASS = NA_character_
      ))
    }
    tibble(
      geneSet = pid,
      kegg_name = res$NAME %>% str_remove(" - Homo sapiens \\(human\\)$"),
      CLASS = if (!is.null(res$CLASS)) res$CLASS[[1]] else NA_character_
    )
  })
  kegg_info <- bind_rows(out_list)

  # Parse CLASS into levels
  kegg_info2 <- kegg_info |>
    mutate(
      CLASS = if_else(is.na(CLASS), "", CLASS),
      TopClass = str_split(CLASS, ";\\s*") %>% map_chr(~ .x[1] %||% NA_character_),
      SubClass = str_split(CLASS, ";\\s*") %>% map_chr(~ ifelse(length(.x) >= 2, .x[2], NA_character_))
    )

  # Join back
  webgestalt2 <- webgestalt |>
    left_join(kegg_info2, by = "geneSet")
}

